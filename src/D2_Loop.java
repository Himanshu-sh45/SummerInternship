public class D2_Loop {
    public static void main(String[] args) {
        int i =2;
        while(i<5){
            System.out.println(i);
            i++;
        }

        int j=21;
        do{
            System.out.println(j);
            j++;
        }while(j<26);


        for(int k=2;k<=5;k++) {
            System.out.println(k);
        }
    }
}